import streamlit as st
import pandas as pd
import joblib

model = joblib.load("machine_failure_prediction_model.pkl")
st.title("Machine failure prediction system")

st.write("you can enter the data from sensor and check whether the machine will work or not under certain condition")


footfall = st.number_input("football")
tempMode = st.number_input("tempMode")
AQ = st.number_input("AQ")
USS = st.number_input("USS")
CS = st.number_input("CS")
VOC = st.number_input("VOC")
RP = st.number_input("RP")
IP = st.number_input("IP")
Temperature = st.number_input("Temperature")

df = pd.DataFrame({
    "footfall":[tempMode],
    "AQ":[AQ],
    "USS":[USS],
    "CS":[CS],
    "VOC":[VOC],
    "RP":[RP],
    "IP":[IP],
    "Temperature":[Temperature]
})

if st.button("Predict"):
  prediction = model.predict(df)
  if prediction[0] == 1:
    st.error("Machine will fail")
  else:
      st.succs("Machine wil work")
